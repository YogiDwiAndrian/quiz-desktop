    private void mulaiBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mulaiBtnActionPerformed
        String nama = namaText.getText();
        
        if(nama.equals("")){
            JOptionPane.showMessageDialog(null, "Nama tidak boleh kosong", "Error", JOptionPane.INFORMATION_MESSAGE);
        }else if(nama.length() < 6){
            JOptionPane.showMessageDialog(null, "Harus lebih dari 5 karakter", "Error", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            
            if(Query.mulaiTest(nama)){
               this.setVisible(false);
               
               HalamanPilihan halamanPilihan = new HalamanPilihan();
               halamanPilihan.setResizable(false);
               halamanPilihan.setVisible(true);
               halamanPilihan.setLocationRelativeTo(null);
            
               
            }
            
            
        }
    }//GEN-LAST:event_mulaiBtnActionPerformed

