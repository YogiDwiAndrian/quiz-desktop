    private void loadRank(){
        String[] rank = {"1 st", "2 nd", "3 rd"};
        
        try{
        
            ResultSet rs = Query.getTopRank(1);
            
            
            
            DefaultTableModel modelIQ = new DefaultTableModel();

            modelIQ.addColumn("Nama");
            modelIQ.addColumn("Skor");
            modelIQ.addColumn("Sisa Waktu");
            modelIQ.addColumn("Rank");

            
            int i = 0;
            while(rs.next()){
                modelIQ.addRow(new Object[]{rs.getString(1), rs.getString(2), rs.getString(3), rank[i++]});
            }
            
            testIQRank.setModel(modelIQ);
            
            rs = Query.getTopRank(2);
            
            DefaultTableModel modelPsikotes = new DefaultTableModel();

            modelPsikotes.addColumn("Nama");
            modelPsikotes.addColumn("Skor");
            modelPsikotes.addColumn("Sisa Waktu");
            modelPsikotes.addColumn("Rank");
            
            i = 0;
            while(rs.next()){
                modelPsikotes.addRow(new Object[]{rs.getString(1), rs.getString(2), rs.getString(3), rank[i++]});
            }
            
           psikotestRank.setModel(modelPsikotes);
           
           rs = Query.getTopRank(3);
            
            DefaultTableModel modelLogika = new DefaultTableModel();

            modelLogika.addColumn("Nama");
            modelLogika.addColumn("Skor");
            modelLogika.addColumn("Sisa Waktu");
            modelLogika.addColumn("Rank");
            
            i = 0;
            while(rs.next()){
                modelLogika.addRow(new Object[]{rs.getString(1), rs.getString(2), rs.getString(3), rank[i++]});
            }
            
           testLogikaRank.setModel(modelLogika);
           
           rs = Query.getTopRank(4);
            
            DefaultTableModel modelUmum = new DefaultTableModel();

            modelUmum.addColumn("Nama");
            modelUmum.addColumn("Skor");
            modelUmum.addColumn("Sisa Waktu");
            modelUmum.addColumn("Rank");
            
            i = 0;
            while(rs.next()){
                modelUmum.addRow(new Object[]{rs.getString(1), rs.getString(2), rs.getString(3), rank[i++]});
            }
            
           testUmumRank.setModel(modelUmum);
           
           
            
        
        }catch(SQLException e){
            
        }
    }

